// Helper to get products by category
export function getProductsByCategory(category: string): Product[] {
  return mockProducts.filter(p => p.category === category);
}

// Helper to get random featured products
export function getFeaturedProducts(count: number = 5): Product[] {
  const shuffled = [...mockProducts].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}

// Helper to search products
export function searchMockProducts(query: string): Product[] {
  const lowerQuery = query.toLowerCase();
  return mockProducts.filter(
    p =>
      p.name.toLowerCase().includes(lowerQuery) ||
      p.description?.toLowerCase().includes(lowerQuery) ||
      p.sku.toLowerCase().includes(lowerQuery)
  );
}

// Helper to get product by barcode
export function getProductByBarcode(barcode: string): Product | undefined {
  return mockProducts.find(p => p.barcode === barcode);
}
